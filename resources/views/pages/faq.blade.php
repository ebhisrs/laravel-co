      <div class="accordion-body">
      At the end of each business day at 5PM (EST), FXDD will automatically rollover or swap, all existing open positions into the next spot settlement date. FXDD will debit or credit a client's account, depending on the interest rate differential between the base currency and the counter currency; and the direction of a client's position. If the client, for example, is long a currency pair where the overnight rate for the base currency is higher than the rate for the counter currency a client will earn a small credit for positions held overnight. If the opposite is true, a client account will be debited for the difference in the interest rate differential. The fundamental reason for this is: should a client be long on a higher yielding currency, the client should benefit from being able to invest and earn a higher return overnight than what the client has to pay for being short the lower yielding currency. Note that instruments with a two-day settlement period will chagerd triple swap on Wednesday. Therefore, trades held past 5pm EST Wednesday, which are considered opened on Thursday trading day will have triple rollover applied to their position. The reason is becuase the trade that occurred on Thursday rolled through the weekend because the banks are closed on Saturday and Sunday. This is an industry wide standard. View our rates here.

